export function getGalleryItemsByCategory(category: string): GalleryItem[] {
  if (category === 'all') {
    return galleryItems.filter(item => item.isFeatured).concat(
      galleryItems.filter(item => !item.isFeatured)
    );
  }
  return galleryItems.filter(item => item.category === category);
}

export function getFeaturedGalleryItems(): GalleryItem[] {
  return galleryItems.filter(item => item.isFeatured);
}

export function getGalleryItemById(id: string): GalleryItem | undefined {
  return galleryItems.find(item => item.id === id);
}

export function updateCategoryItemCounts(): GalleryCategory[] {
  return galleryCategories.map(category => {
    if (category.id === 'all') {
      return { ...category, itemCount: galleryItems.length };
    }
    const count = galleryItems.filter(item => item.category === category.id).length;
    return { ...category, itemCount: count };
  });
}
